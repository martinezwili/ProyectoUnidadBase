    public static int consultar_coddigo(ObjectContainer base, String esp_codigo){
        Especialidad espe = new Especialidad(esp_codigo, null, null);
        ObjectSet resul = base.get(espe);
        return resul.size();
    }
    
    public static int consultar_nombre(ObjectContainer base, String esp_nombre){
        Especialidad espe = new Especialidad(null, esp_nombre, null);
        ObjectSet resul = base.get(espe);
        return resul.size();
    }
