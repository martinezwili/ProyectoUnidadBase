    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // TODO add your handling code here:
        ObjectContainer DB = Db4o.openFile("C:\\Users\\MAWIL\\Downloads\\BD Access\\M2C\\clinica2.yap"); 
        if(tfbuscar.getText() != null){
            if(consultar_coddigo(DB, tfbuscar.getText()) != 0){
                Especialidad espe = new Especialidad(tfbuscar.getText(), null, null);
                ObjectSet resul = DB.get(espe);

                while(resul.hasNext()){
                    tadoctor.setText(resul.next().toString());
                }
            }else if(consultar_coddigo(DB, tfbuscar.getText()) != 0){
                Especialidad espe = new Especialidad(tfbuscar.getText(), null, null);
                ObjectSet resul = DB.get(espe);

                while(resul.hasNext()){
                    tadoctor.setText(resul.next().toString());
                }
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "No se encntraron datos");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "INGRESE EL CODIGO O NOMBRE DE LA ESPECIALIDAD QUE DESEA BUSCAR");
        }
        
    }//GEN-LAST:event_btnbuscarActionPerformed

